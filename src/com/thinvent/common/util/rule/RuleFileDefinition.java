package com.thinvent.common.util.rule;

public class RuleFileDefinition {

	private String country;
	private String portlet;
	private String mode;
	private int sequence;
	private String drlfile;
	private String dslfile;
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getPortlet() {
		return portlet;
	}
	public void setPortlet(String portlet) {
		this.portlet = portlet;
	}
	public String getMode() {
		return mode;
	}
	public void setMode(String mode) {
		this.mode = mode;
	}
	public int getSequence() {
		return sequence;
	}
	public void setSequence(int sequence) {
		this.sequence = sequence;
	}
	public String getDrlfile() {
		return drlfile;
	}
	public void setDrlfile(String drlfile) {
		this.drlfile = drlfile;
	}
	public String getDslfile() {
		return dslfile;
	}
	public void setDslfile(String dslfile) {
		this.dslfile = dslfile;
	}
	
	
}
